import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:google_fonts/google_fonts.dart';

// Language enum
enum AppLanguage {
  lao,
  english,
}

// Language state
class LanguageState {
  final AppLanguage language;
  final Locale locale;
  final String languageCode;

  LanguageState({
    required this.language,
    required this.locale,
    required this.languageCode,
  });

  LanguageState copyWith({
    AppLanguage? language,
    Locale? locale,
    String? languageCode,
  }) {
    return LanguageState(
      language: language ?? this.language,
      locale: locale ?? this.locale,
      languageCode: languageCode ?? this.languageCode,
    );
  }
}

// Language provider
final languageProvider = StateProvider<LanguageState>((ref) {
  return LanguageState(
    language: AppLanguage.lao,
    locale: const Locale('lo'),
    languageCode: 'lo',
  );
});

// Function to switch language
void switchLanguage(WidgetRef ref, AppLanguage language) {
  final languageState = ref.read(languageProvider);
  
  switch (language) {
    case AppLanguage.lao:
      ref.read(languageProvider.notifier).state = languageState.copyWith(
        language: AppLanguage.lao,
        locale: const Locale('lo'),
        languageCode: 'lo',
      );
      break;
    case AppLanguage.english:
      ref.read(languageProvider.notifier).state = languageState.copyWith(
        language: AppLanguage.english,
        locale: const Locale('en'),
        languageCode: 'en',
      );
      break;
  }
}

